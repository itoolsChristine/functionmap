def main():
    import argparse
    import sys

    ap = argparse.ArgumentParser(
        prog='describe',
        description='Universal description engine for function maps. '
                    'Use --stats or --dry-run to analyze description coverage. '
                    'For full categorization + markdown generation, use categorize.py instead.'
    )
    ap.add_argument(
        '--project', default='',
        help='Project name (default: most recent in ~/.claude/functionmap)'
    )
    ap.add_argument(
        '--dry-run', action='store_true',
        help='Preview descriptions without writing files'
    )
    ap.add_argument(
        '--stats', action='store_true',
        help='Show description coverage statistics'
    )
    args = ap.parse_args()

    base_dir = Path.home() / '.claude' / 'functionmap'

    # Find project
    if args.project:
        project_dir = base_dir / args.project
    else:
        meta_files = list(base_dir.glob('*/_meta.json'))
        if not meta_files:
            print("[describe] No projects found", file=sys.stderr)
            return 1
        meta_files.sort(key=lambda p: p.stat().st_mtime, reverse=True)
        project_dir = meta_files[0].parent

    project_name = project_dir.name
    print(f"[describe] Project: {project_name}")

    # Load functions
    functions_json = project_dir / '_functions.json'
    if not functions_json.exists():
        print(f"[describe] ERROR: {functions_json} not found", file=sys.stderr)
        return 1

    with open(functions_json, 'r', encoding='utf-8') as f:
        functions = json.load(f)

    print(f"[describe] Functions: {len(functions)}")

    if args.stats:
        _print_stats(functions)
        return 0

    if args.dry_run:
        _preview_descriptions(functions)
        return 0

    # Default: show stats (full run is handled by categorize.py)
    print("[describe] Use --stats for coverage analysis or --dry-run for preview.")
    print("[describe] For full categorization + markdown generation, run categorize.py")
    _print_stats(functions)
    return 0


def _print_stats(functions: List[dict]) -> None:
    """Print description coverage statistics."""
    total = len(functions)
    has_doc = sum(1 for f in functions if (f.get('doc') or '').strip())
    has_summary = sum(
        1 for f in functions
        if (f.get('summary') or '').strip()
        and not f['summary'].startswith('@')
    )

    # Test inference coverage
    has_verb = 0
    for f in functions:
        verb, _ = _extract_verb_and_noun(f['short_name'])
        if verb and verb in VERB_TEMPLATES:
            has_verb += 1

    inferred = 0
    for f in functions:
        desc = describe_function(f)
        if desc and 'operations' not in desc:  # "operations" = weak fallback
            inferred += 1

    print(f"\n=== Description Coverage ===")
    print(f"  Total functions:     {total}")
    print(f"  Has docblock:        {has_doc} ({has_doc*100//total}%)")
    print(f"  Has useful summary:  {has_summary} ({has_summary*100//total}%)")
    print(f"  Verb-inferable:      {has_verb} ({has_verb*100//total}%)")
    print(f"  Total describable:   {inferred} ({inferred*100//total}%)")
    print(f"  Needs fallback:      {total - inferred} ({(total-inferred)*100//total}%)")


def _preview_descriptions(functions: List[dict]) -> None:
    """Preview generated descriptions for a sample of functions."""
    # Show a mix: some with docs, some without
    samples = []

    # Pick 5 with docs
    with_docs = [f for f in functions if (f.get('doc') or '').strip()][:5]
    samples.extend(with_docs)

    # Pick 10 without docs but with recognizable verbs
    without_docs = [
        f for f in functions
        if not (f.get('doc') or '').strip()
        and _extract_verb_and_noun(f['short_name'])[0] in VERB_TEMPLATES
    ][:10]
    samples.extend(without_docs)

    # Pick 5 without docs and no verb match (hardest case)
    hard_cases = [
        f for f in functions
        if not (f.get('doc') or '').strip()
        and _extract_verb_and_noun(f['short_name'])[0] not in VERB_TEMPLATES
    ][:5]
    samples.extend(hard_cases)

    print(f"\n=== Description Preview ({len(samples)} samples) ===\n")
    for func in samples:
        name = func['short_name']
        has_doc = bool((func.get('doc') or '').strip())
        source = 'DOCBLOCK' if has_doc else 'INFERRED'
        desc = describe_function(func)
        print(f"  [{source}] {name}")
        print(f"    -> {desc}")
        print()


