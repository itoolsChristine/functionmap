def generate_enhanced_category_markdown(cat_name: str, funcs: List[dict]) -> str:
    """Generate enhanced markdown with rich descriptions for a category."""
    title = cat_name.replace('-', ' ').replace('_', ' ').title()
    description = describe_category(cat_name, funcs)

    lines = [
        f"# {title}",
        "",
        description,
        "",
        f"**Function count:** {len(funcs)}",
        "",
        "---",
        "",
    ]

    # Sort functions by name
    funcs_sorted = sorted(funcs, key=lambda f: f['short_name'].lower())

    for func in funcs_sorted:
        # Function header
        visibility = f"{func['visibility']} " if func.get('visibility') else ''
        static = 'static ' if func.get('is_static') else ''
        async_marker = 'async ' if func.get('is_async') else ''

        signature = f"{visibility}{static}{async_marker}{func['short_name']}({func['params']})"
        if func.get('return_type') and func['return_type'] != 'unknown':
            signature += f": {func['return_type']}"

        lines.append(f"## {func['short_name']}")
        lines.append("")

        # Rich description
        func_desc = describe_function(func)
        if func_desc:
            lines.append(func_desc)
            lines.append("")

        lines.append(f"**Signature:** `{signature}`")
        lines.append("")

        # Location
        location = f"{func['file']}:{func['line_start']}"
        if func['line_end'] != func['line_start']:
            location += f"-{func['line_end']}"
        lines.append(f"**Location:** `{location}`")
        lines.append("")

        # Namespace/Class
        if func.get('namespace'):
            lines.append(f"**Namespace:** `{func['namespace']}`")
            lines.append("")
        if func.get('class_name'):
            lines.append(f"**Class:** `{func['class_name']}`")
            lines.append("")

        # Attributes
        if func.get('attributes'):
            attrs = ', '.join(func['attributes'])
            lines.append(f"**Attributes:** `{attrs}`")
            lines.append("")

        lines.append("---")
        lines.append("")

    return '\n'.join(lines)


def generate_enhanced_index(
    categories: Dict[str, List[dict]], project_name: str
) -> str:
    """Generate enhanced project index with category descriptions."""
    total_funcs = sum(len(funcs) for funcs in categories.values())

    lines = [
        f"# {project_name} Function Map",
        "",
        f"**Total functions:** {total_funcs}",
        f"**Categories:** {len(categories)}",
        "",
        "## Categories",
        "",
    ]

    # Group categories by base name
    base_groups: Dict[str, List[Tuple[str, List[dict]]]] = defaultdict(list)
    for cat_name, funcs in categories.items():
        base = re.sub(r'-(part-\d+|[a-z](?:-[a-z])?)$', '', cat_name)
        base_groups[base].append((cat_name, funcs))

    for base, cats in sorted(base_groups.items()):
        total = sum(len(funcs) for _, funcs in cats)
        base_title = base.replace('-', ' ').replace('_', ' ').title()

        # Get description for the base category
        all_funcs = []
        for _, funcs in cats:
            all_funcs.extend(funcs)
        desc = describe_category(base, all_funcs)
        # Truncate for index listing
        short_desc = desc[:120] + '...' if len(desc) > 120 else desc

        if len(cats) == 1:
            cat_name, funcs = cats[0]
            lines.append(f"### [{base_title}]({cat_name}.md) ({len(funcs)} functions)")
            lines.append(f"  {short_desc}")
            lines.append("")
        else:
            lines.append(f"### {base_title} ({total} functions)")
            lines.append(f"  {short_desc}")
            lines.append("")
            for cat_name, funcs in sorted(cats):
                title = cat_name.replace('-', ' ').replace('_', ' ').title()
                lines.append(f"  - [{title}]({cat_name}.md) ({len(funcs)} functions)")
            lines.append("")

    lines.extend([
        "## Usage",
        "",
        "1. Browse categories above to find relevant functions",
        "2. Click category link to see function signatures, descriptions, and locations",
        "3. Use location info to read source code for implementation details",
        "",
    ])

    return '\n'.join(lines)


