def is_third_party(fn: dict, taxonomy: dict) -> bool:
    """Check if a function belongs to excluded third-party code.

    Uses third_party patterns from the taxonomy routing rules.
    """
    fp = fn["file"].replace("\\", "/")
    rules = taxonomy.get("routing_rules", {})

    for rule in rules.get("third_party", []):
        if _matches_pattern(fp, fn, rule):
            return True
    return False


def categorize_function(
    fn: dict, taxonomy: dict, enrichment_entry: Optional[dict] = None
) -> Tuple[str, str]:
    """
    Categorize a function using routing rules from taxonomy config.

    Returns (top_category, subcategory) tuple.

    Rules are evaluated in priority order -- first match wins:
      1. Third-party exclusion (handled separately via is_third_party)
      2. Deprecated/legacy patterns
      3. Namespace routes (prefix matching)
      4. Directory routes (path prefix matching)
      5. File routes (exact file matching)
      6. Keyword routes (function name matching)
      6.5. Fallback file routing
      6.7. Enrichment category_hint (for otherwise-uncategorized functions)
      7. Uncategorized fallback
    """
    fp = fn["file"].replace("\\", "/")
    ns = fn.get("namespace") or ""
    cls = fn.get("class_name") or ""
    name = fn.get("short_name") or ""

    rules = taxonomy.get("routing_rules", {})

    # --- Rule 2: Deprecated/legacy detection ---
    for rule in rules.get("deprecated", []):
        if _matches_pattern(fp, fn, rule):
            cat = rule.get("category", "deprecated")
            sub = rule.get("subcategory", "other")
            return (cat, sub)

    # --- Rule 3: Namespace routing ---
    # Routes are evaluated in order -- more specific rules should come first
    for route in rules.get("namespace_routes", []):
        pattern = route.get("pattern", "")
        if not ns or not ns.startswith(pattern):
            continue

        # Optional class filter for sub-routing within a namespace
        class_filter = route.get("class")
        if class_filter and cls != class_filter:
            continue

        # Optional file_contains filter for sub-routing within a namespace
        file_filter = route.get("file_contains")
        if file_filter and file_filter.lower() not in fp.lower():
            continue

        cat = route["category"]
        sub = route.get("subcategory", "core")
        return (cat, sub)

    # --- Rule 4: Directory routing ---
    for route in rules.get("directory_routes", []):
        pattern = route.get("pattern", "")
        if fp.startswith(pattern):
            cat = route["category"]
            sub = route.get("subcategory", "core")
            return (cat, sub)

    # --- Rule 5: File routing ---
    for route in rules.get("file_routes", []):
        file_pattern = route.get("file", "")
        if fp == file_pattern:
            cat = route["category"]
            sub = route.get("subcategory", "core")
            return (cat, sub)

    # --- Rule 5.5: File map (dict lookup, O(1)) ---
    file_map = rules.get("file_map", {})
    if fp in file_map:
        entry = file_map[fp]
        cat = entry["category"]
        sub = entry.get("subcategory", "core")
        return (cat, sub)

    # --- Rule 6: Keyword routing ---
    name_lower = name.lower()
    for route in rules.get("keyword_routes", []):
        keywords = route.get("keywords", [])
        if any(kw.lower() in name_lower for kw in keywords):
            cat = route["category"]
            sub = route.get("subcategory", "core")
            return (cat, sub)

    # --- Rule 6.5: Fallback file routing (catch-all for multi-category files) ---
    for route in rules.get("fallback_file_routes", []):
        file_pattern = route.get("file", "")
        if fp == file_pattern:
            cat = route["category"]
            sub = route.get("subcategory", "core")
            return (cat, sub)

    # --- Rule 6.7: Enrichment category_hint (for otherwise-uncategorized functions) ---
    if enrichment_entry:
        hint = enrichment_entry.get("category_hint")
        if hint and hint in taxonomy.get("categories", {}):
            return (hint, "enriched")

    # --- Rule 7: Uncategorized fallback ---
    return ("uncategorized", "other")


# =============================================================================
# Category descriptions from taxonomy
# =============================================================================

def get_category_description(cat_key: str, taxonomy: dict) -> str:
    """Get description for a category from taxonomy config.

    Supports both "top-category" and "top-category/subcategory" key formats.
    Falls back to a generated description if no taxonomy entry exists.

    Subcategory values in the taxonomy can be either:
      - A string: the description directly
      - A dict with a "description" key
    """
    categories = taxonomy.get("categories", {})

    # Try "top/sub" format
    if "/" in cat_key:
        top, sub = cat_key.split("/", 1)
        top_cat = categories.get(top, {})
        subcats = top_cat.get("subcategories", {})
        if sub in subcats:
            sub_entry = subcats[sub]
            if isinstance(sub_entry, dict):
                return sub_entry.get("description", "")
            return str(sub_entry)
        # Fall back to top-level description
        if "description" in top_cat:
            return top_cat["description"]

    # Try top-level match
    if cat_key in categories:
        cat_entry = categories[cat_key]
        if isinstance(cat_entry, dict):
            return cat_entry.get("description", "")
        return str(cat_entry)

    # Generate fallback
    readable = cat_key.replace("-", " ").replace("_", " ")
    return f"Functions related to {readable}."


# =============================================================================
# Function description (simple fallback if describe.py is unavailable)
# =============================================================================

def extract_function_description(fn: dict) -> str:
    """Extract or infer a description for a function.

    Uses describe.py's rich engine if available, otherwise falls back
    to simple name-based inference.

    Priority:
    1. Rich description engine (describe.py)
    2. Docblock summary (if available)
    3. Inferred from function name
    """
    # Try rich description engine first
    if _rich_describe is not None:
        try:
            return _rich_describe(fn)
        except Exception:
            pass

    # Fallback: use existing summary from docblock
    if fn.get("summary"):
        summary = fn["summary"].strip()
        if not summary.startswith("@"):
            return summary

    # Fallback: infer from function name
    name = fn["short_name"]
    return_type = fn.get("return_type", "")

    readable = _name_to_readable(name)
    parts = [readable]

    if return_type and return_type not in ("unknown", "void", "mixed"):
        parts.append(f"Returns {return_type}.")

    return ". ".join(parts)


def _name_to_readable(name: str) -> str:
    """Convert function name to readable description."""
    clean_name = name.lstrip("_")
    if not clean_name:
        clean_name = name

    # Handle PHP magic methods
    if clean_name.startswith("__") or name.startswith("__"):
        magic = name.replace("__", "")
        if magic:
            return f"Magic method: {magic}"
        return name

    name = clean_name

    prefixes = {
        "get": "Gets", "set": "Sets", "is": "Checks if",
        "has": "Checks whether it has", "can": "Checks if it can",
        "should": "Determines if it should", "create": "Creates",
        "make": "Creates", "build": "Builds", "add": "Adds",
        "remove": "Removes", "delete": "Deletes", "update": "Updates",
        "save": "Saves", "load": "Loads", "find": "Finds",
        "search": "Searches for", "show": "Displays", "hide": "Hides",
        "render": "Renders", "display": "Displays", "format": "Formats",
        "parse": "Parses", "validate": "Validates", "check": "Checks",
        "init": "Initializes", "setup": "Sets up", "reset": "Resets",
        "clear": "Clears", "enable": "Enables", "disable": "Disables",
        "register": "Registers", "handle": "Handles", "process": "Processes",
        "convert": "Converts", "apply": "Applies", "execute": "Executes",
        "run": "Runs", "start": "Starts", "stop": "Stops",
        "send": "Sends", "fetch": "Fetches", "upload": "Uploads",
        "download": "Downloads", "import": "Imports", "export": "Exports",
        "encode": "Encodes", "decode": "Decodes", "log": "Logs",
        "filter": "Filters", "sort": "Sorts", "merge": "Merges",
        "count": "Counts", "normalize": "Normalizes", "sanitize": "Sanitizes",
        "escape": "Escapes", "clean": "Cleans", "open": "Opens",
        "close": "Closes", "connect": "Connects", "on": "Handler for",
    }

    if "_" in name:
        parts = name.split("_")
    else:
        parts = re.findall(r"[A-Z]?[a-z]+|[A-Z]+(?=[A-Z][a-z]|\d|\b)", name)
        if not parts:
            parts = [name]

    if not parts:
        return name

    first = parts[0].lower()
    rest = " ".join(p.lower() for p in parts[1:]) if len(parts) > 1 else ""

    if first in prefixes:
        verb = prefixes[first]
        if rest:
            return f"{verb} {rest}"
        return verb
    else:
        all_parts = " ".join(p.lower() for p in parts)
        return all_parts.capitalize()


