# Purpose-based category splitting (universal)
# =============================================================================

MAX_CATEGORY_SIZE = 250


def split_large_categories(
    categories: Dict[str, Dict[str, List[dict]]]
) -> Dict[str, Dict[str, List[dict]]]:
    """
    Split subcategories exceeding MAX_CATEGORY_SIZE by purpose.

    Uses file path clustering to group related functions, NOT alphabetical
    splitting. Functions from the same source file stay together.
    """
    result = {}

    for top_cat, subcats in categories.items():
        result[top_cat] = {}

        for sub_name, funcs in subcats.items():
            if len(funcs) <= MAX_CATEGORY_SIZE:
                result[top_cat][sub_name] = funcs
                continue

            # Group by source file
            by_file: Dict[str, List[dict]] = defaultdict(list)
            for f in funcs:
                by_file[f["file"].replace("\\", "/")].append(f)

            sorted_files = sorted(by_file.keys())

            chunks: List[Tuple[str, List[dict]]] = []
            current_chunk: List[dict] = []
            current_label_parts: List[str] = []

            for filepath in sorted_files:
                file_funcs = by_file[filepath]

                if len(current_chunk) + len(file_funcs) > MAX_CATEGORY_SIZE and current_chunk:
                    chunk_label = _derive_chunk_label(current_label_parts)
                    chunks.append((chunk_label, current_chunk))
                    current_chunk = []
                    current_label_parts = []

                current_chunk.extend(file_funcs)
                current_label_parts.append(filepath)

            if current_chunk:
                chunk_label = _derive_chunk_label(current_label_parts)
                chunks.append((chunk_label, current_chunk))

            if len(chunks) == 1:
                result[top_cat][sub_name] = funcs
            else:
                for idx, (label, chunk_funcs) in enumerate(chunks, 1):
                    new_name = f"{sub_name}-{label}" if label else f"{sub_name}-part-{idx}"
                    if new_name in result[top_cat]:
                        new_name = f"{sub_name}-part-{idx}"
                    result[top_cat][new_name] = chunk_funcs

    return result


def _derive_chunk_label(file_paths: List[str]) -> str:
    """Derive a meaningful label from a list of file paths."""
    if not file_paths:
        return ""

    basenames = [p.split("/")[-1].replace(".php", "").replace(".js", "").replace(".ts", "") for p in file_paths]

    # If all from same directory, use directory name
    dirs = set(("/".join(p.split("/")[:-1]) for p in file_paths))
    if len(dirs) == 1:
        dir_name = list(dirs)[0].split("/")[-1] if list(dirs)[0] else ""
        if dir_name and dir_name not in ("lib", "src", "plugins", "vendor"):
            return dir_name.lower().replace(" ", "-")

    # Use first letter range
    if len(basenames) >= 2:
        first_letter = basenames[0][0].upper()
        last_letter = basenames[-1][0].upper()
        if first_letter != last_letter:
            return f"{first_letter}-{last_letter}"
        return f"{first_letter}"

    return basenames[0][:20].lower() if basenames else ""


