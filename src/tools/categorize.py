#!/usr/bin/env python3
"""
Function Map Categorizer - Universal config-driven categorization engine.

Reads _functions.json and _taxonomy.json to create hierarchical categorized
markdown files. Routes functions to semantic domain categories based on routing
rules defined in the project-specific taxonomy configuration.

All routing logic is data-driven from _taxonomy.json. No hard-coded project
knowledge. The taxonomy is designed by Claude during the function map workflow,
tailored to each project's unique structure and domain.

Routing rule priority (evaluated in order, first match wins):
  1. Third-party exclusion (patterns from taxonomy)
  2. Deprecated/legacy detection (patterns from taxonomy)
  3. Namespace routing (prefix matching from taxonomy)
  4. Directory routing (path prefix matching from taxonomy)
  5. File routing (exact file matching from taxonomy)
  6. Keyword routing (function name matching from taxonomy)
  7. Uncategorized fallback

Produces:
  - Category .md files with rich descriptions
  - Hierarchical index with category descriptions
  - Optional third-party-bundled.md reference

Usage:
    python categorize.py                          # Process latest project
    python categorize.py --project my-project      # Process specific project
"""

