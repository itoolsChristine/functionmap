def load_taxonomy(project_dir: Path) -> dict:
    """Load the taxonomy config for a project.

    The _taxonomy.json file is generated by Claude during the function map
    workflow. It contains project-specific categories and routing rules.

    If no taxonomy exists, returns a minimal fallback that routes everything
    to 'uncategorized'.
    """
    taxonomy_path = project_dir / "_taxonomy.json"
    if not taxonomy_path.exists():
        print(f"[categorize] WARNING: No _taxonomy.json found in {project_dir}")
        print(f"[categorize] All functions will be uncategorized.")
        print(f"[categorize] Run the taxonomy design phase first to create proper categories.")
        return _minimal_taxonomy()

    with open(taxonomy_path, "r", encoding="utf-8") as f:
        taxonomy = json.load(f)

    # Validate basic structure
    if "categories" not in taxonomy:
        print(f"[categorize] WARNING: _taxonomy.json missing 'categories' key")
        taxonomy["categories"] = {}
    if "routing_rules" not in taxonomy:
        print(f"[categorize] WARNING: _taxonomy.json missing 'routing_rules' key")
        taxonomy["routing_rules"] = {}

    return taxonomy


