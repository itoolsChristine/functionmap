def main():
    import argparse

    ap = argparse.ArgumentParser(
        prog="categorize",
        description="Universal config-driven function categorization engine."
    )
    ap.add_argument(
        "--project", default="",
        help="Project name (default: most recent in ~/.claude/functionmap)"
    )
    ap.add_argument(
        "--max-category-size", type=int, default=250,
        help="Max functions per subcategory file before auto-splitting (default: 250)"
    )
    args = ap.parse_args()

    global MAX_CATEGORY_SIZE
    MAX_CATEGORY_SIZE = args.max_category_size

    home = Path.home()
    base = home / ".claude" / "functionmap"

    # Find project
    if args.project:
        project_dir = base / args.project
    else:
        meta_files = list(base.glob("*/_meta.json"))
        if not meta_files:
            print("[categorize] No projects found")
            return
        meta_files.sort(key=lambda p: p.stat().st_mtime, reverse=True)
        project_dir = meta_files[0].parent

    project_name = project_dir.name
    print(f"[categorize] Project: {project_name}")

    # Load taxonomy
    taxonomy = load_taxonomy(project_dir)
    print(f"[categorize] Taxonomy: {len(taxonomy.get('categories', {}))} categories, "
          f"{sum(len(r) for r in taxonomy.get('routing_rules', {}).values())} routing rules")

    # Load functions
    functions_json = project_dir / "_functions.json"
    if not functions_json.exists():
        print(f"[categorize] ERROR: {functions_json} not found")
        return

    with open(functions_json, "r", encoding="utf-8") as f:
        all_functions = json.load(f)

    print(f"[categorize] Total functions: {len(all_functions)}")

    # Load enrichment (optional sidecar data)
    enrichment = load_enrichment(project_dir)
    enrichment_index = {}
    if enrichment:
        enrichment_index = build_enrichment_index(enrichment, all_functions)
        matched = len(enrichment_index)
        total_entries = len(enrichment.get("entries", {}))
        source = enrichment.get("source", "unknown")
        print(f"[categorize] Enrichment: {total_entries} entries, "
              f"{matched} matched to functions (source: {source})")

    # Separate third-party from first-party (Rule 1)
    third_party = [fn for fn in all_functions if is_third_party(fn, taxonomy)]
    first_party = [fn for fn in all_functions if not is_third_party(fn, taxonomy)]
    print(f"[categorize] Third-party (excluded): {len(third_party)}")
    print(f"[categorize] First-party (categorizing): {len(first_party)}")

    # Map third-party libraries to shared location
    mapped_libs = {}
    if third_party and _has_thirdparty:
        print("[categorize] Mapping third-party libraries...")
        # Load source root from _meta.json for metadata
        meta_path = project_dir / "_meta.json"
        source_root = ""
        if meta_path.exists():
            try:
                meta = json.load(open(meta_path, encoding="utf-8"))
                source_root = meta.get("root_path", "")
            except (json.JSONDecodeError, ValueError):
                pass

        lib_groups = group_by_library(third_party, taxonomy, project_root=source_root)
        for key, group in lib_groups.items():
            if key == "_unresolved":
                unresolved_count = len(group.get("functions", []))
                if unresolved_count > 0:
                    print(f"[categorize] Third-party unresolved: {unresolved_count} functions "
                          f"(no library/version detected)")
                continue
            lib_name = group["library"]
            version = group["version"]
            result = map_library(
                lib_name, version, group["functions"],
                project_name, base, source_root=source_root,
            )
            if result:
                mapped_libs[key] = result
                tp_update_index(base, lib_name, version, result, project_name)

        if mapped_libs:
            print(f"[categorize] Mapped {len(mapped_libs)} third-party libraries")

    # Categorize using routing rules
    print("[categorize] Categorizing by routing rules...")
    raw_categories: Dict[str, Dict[str, List[dict]]] = defaultdict(lambda: defaultdict(list))

    for fn in first_party:
        fn_key = f"{fn['file']}::{fn['short_name']}::{fn['line_start']}"
        enrich_entry = enrichment_index.get(fn_key)
        top_cat, sub_cat = categorize_function(fn, taxonomy, enrichment_entry=enrich_entry)
        raw_categories[top_cat][sub_cat].append(fn)

    top_count = len(raw_categories)
    sub_count = sum(len(subs) for subs in raw_categories.values())
    print(f"[categorize] Raw: {top_count} top-level, {sub_count} subcategories")

    # Merge very small subcategories (< 3 functions) into parent "other"
    print("[categorize] Consolidating tiny subcategories...")
    categories: Dict[str, Dict[str, List[dict]]] = {}
    for top_cat, subcats in raw_categories.items():
        categories[top_cat] = {}
        for sub_name, funcs in subcats.items():
            if len(funcs) < 3 and sub_name not in ("core", "other"):
                categories[top_cat].setdefault("other", []).extend(funcs)
            else:
                categories[top_cat][sub_name] = funcs

    sub_count = sum(len(subs) for subs in categories.values())
    print(f"[categorize] After consolidation: {len(categories)} top-level, {sub_count} subcategories")

    # Split oversized categories by purpose
    print("[categorize] Splitting oversized categories by purpose...")
    categories = split_large_categories(categories)
    sub_count = sum(len(subs) for subs in categories.values())
    print(f"[categorize] Final: {len(categories)} top-level, {sub_count} subcategories")

    # Clean old category files (only category -- files and third-party-bundled.md)
    print("[categorize] Cleaning old category files...")
    for md_file in project_dir.glob("*.md"):
        if "--" in md_file.name or md_file.name == "third-party-bundled.md":
            md_file.unlink()

    # Generate category markdown files
    print("[categorize] Generating category files...")
    file_count = 0
    for top_cat, subcats in sorted(categories.items()):
        for sub_name, funcs in sorted(subcats.items()):
            md_content = generate_category_markdown(
                top_cat, sub_name, funcs, taxonomy,
                enrichment_index=enrichment_index,
            )
            filename = f"{top_cat}--{sub_name}.md"
            md_path = project_dir / filename
            md_path.write_text(md_content, encoding="utf-8")
            file_count += 1

    print(f"[categorize] Generated {file_count} category files")

    # Generate third-party summary
    if third_party:
        print("[categorize] Generating third-party summary...")
        tp_content = generate_third_party_summary(third_party, taxonomy)
        # Enhance with mapped library links
        if mapped_libs and _has_thirdparty:
            tp_content = enhance_third_party_summary(
                tp_content, mapped_libs, third_party, taxonomy
            )
        tp_path = project_dir / "third-party-bundled.md"
        tp_path.write_text(tp_content, encoding="utf-8")

    # Generate index
    print("[categorize] Generating index...")
    index_content = generate_index(
        categories, project_name, len(third_party), taxonomy,
        enrichment=enrichment,
    )

    # Append third-party library table if any were mapped
    if _has_thirdparty:
        tp_table = generate_third_party_table(base, project_name)
        if tp_table:
            index_content += "\n" + tp_table

    index_path = base / f"{project_name}.md"
    index_path.write_text(index_content, encoding="utf-8")
    print(f"[categorize] Index: {index_path}")

    # Print category summary
    print()
    print("[categorize] === CATEGORY SUMMARY ===")
    for top_cat in sorted(categories.keys()):
        subcats = categories[top_cat]
        total = sum(len(f) for f in subcats.values())
        sub_names = ", ".join(sorted(subcats.keys()))
        print(f"  {top_cat}: {total} functions ({len(subcats)} subcats: {sub_names})")

    # Print overall summary
    total_first_party = sum(
        len(funcs)
        for subcats in categories.values()
        for funcs in subcats.values()
    )
    print()
    print(f"[categorize] === SUMMARY ===")
    print(f"[categorize] Project: {project_name}")
    print(f"[categorize] First-party functions: {total_first_party}")
    print(f"[categorize] Third-party excluded: {len(third_party)}")
    print(f"[categorize] Top-level categories: {len(categories)}")
    print(f"[categorize] Total subcategories: {sub_count}")
    print(f"[categorize] Category files: {file_count}")
    if mapped_libs:
        print(f"[categorize] Third-party libs mapped: {len(mapped_libs)}")
        for key, info in sorted(mapped_libs.items()):
            status = info.get("status", "mapped")
            note = f" (needs docs)" if status == "needs_docs" else ""
            print(f"[categorize]   {key}: {info.get('function_count', 0)} functions{note}")
    if enrichment:
        enriched_routed = sum(
            1 for subcats in categories.values()
            for sub_name, funcs in subcats.items()
            if sub_name == "enriched" for _ in funcs
        )
        print(f"[categorize] Enrichment source: {enrichment.get('source', 'unknown')}")
        if enriched_routed:
            print(f"[categorize] Enrichment rescued from uncategorized: {enriched_routed}")
    print(f"[categorize] Index: ~/.claude/functionmap/{project_name}.md")


if __name__ == "__main__":
    main()
