def _get_usage_examples(top_cat: str, taxonomy: dict) -> list:
    """Get usage examples for a category from taxonomy config."""
    categories = taxonomy.get("categories", {})
    cat_entry = categories.get(top_cat, {})
    if isinstance(cat_entry, dict):
        return cat_entry.get("usage_examples", [])
    return []


def _format_subcategory_title(sub_name: str) -> str:
    """Format subcategory name into a human-readable title.

    Handles letter-range splits like 'admin-crud-A-F' -> 'Admin Crud (A-F)'
    and part splits like 'core-part-2' -> 'Core (Part 2)'.
    """
    range_match = re.match(r'^(.+?)-([A-Z_])-([A-Z])$', sub_name)
    if range_match:
        base = range_match.group(1).replace("-", " ").replace("_", " ").title()
        return f"{base} ({range_match.group(2)}-{range_match.group(3)})"

    letter_match = re.match(r'^(.+?)-([A-Z])$', sub_name)
    if letter_match:
        base = letter_match.group(1).replace("-", " ").replace("_", " ").title()
        return f"{base} ({letter_match.group(2)})"

    part_match = re.match(r'^(.+?)-part-(\d+)$', sub_name)
    if part_match:
        base = part_match.group(1).replace("-", " ").replace("_", " ").title()
        return f"{base} (Part {part_match.group(2)})"

    return sub_name.replace("-", " ").replace("_", " ").title()


def generate_category_markdown(
    top_cat: str, sub_name: str, funcs: List[dict], taxonomy: dict,
    enrichment_index: Optional[dict] = None,
) -> str:
    """Generate markdown content for a category file."""
    cat_key = f"{top_cat}/{sub_name}"
    title = _format_subcategory_title(sub_name)
    top_title = top_cat.replace("-", " ").replace("_", " ").title()
    description = get_category_description(cat_key, taxonomy)

    # Fall back to top-level description if subcategory has generic fallback
    fallback_check = get_category_description("__nonexistent__", taxonomy)
    if description == fallback_check:
        description = get_category_description(top_cat, taxonomy)

    lines = [
        f"# {top_title} > {title}",
        "",
        description,
        "",
    ]

    # Check for variant note (used by typed-variants categories)
    cat_entry = taxonomy.get("categories", {}).get(top_cat, {})
    variant_note = cat_entry.get("variant_note", "") if isinstance(cat_entry, dict) else ""
    if variant_note:
        lines.append(f"> **Note:** {variant_note}")
        lines.append("")

    lines.extend([
        f"**Function count:** {len(funcs)}",
        "",
        "---",
        "",
    ])

    # Usage examples from taxonomy
    examples = _get_usage_examples(top_cat, taxonomy)
    if examples:
        lines.append("## Common Patterns")
        lines.append("")
        for ex in examples:
            if ex.get("title"):
                lines.append(f"### {ex['title']}")
                lines.append("")
            lang = ex.get("language", "php")
            lines.append(f"```{lang}")
            lines.append(ex.get("code", ""))
            lines.append("```")
            lines.append("")
            if ex.get("notes"):
                lines.append(ex["notes"])
                lines.append("")
        lines.append("---")
        lines.append("")

    # Sort functions by name
    funcs.sort(key=lambda f: f["short_name"].lower())

    for func in funcs:
        # Look up enrichment data for this function
        fn_key = f"{func['file']}::{func['short_name']}::{func['line_start']}"
        enrich = enrichment_index.get(fn_key, {}) if enrichment_index else {}

        vis = f"{func['visibility']} " if func.get("visibility") else ""
        static = "static " if func.get("is_static") else ""
        async_m = "async " if func.get("is_async") else ""

        sig = f"{vis}{static}{async_m}{func['short_name']}({func['params']})"
        if func.get("return_type") and func["return_type"] != "unknown":
            sig += f": {func['return_type']}"

        # Heading: use displayName if enrichment provides one
        if enrich.get("displayName"):
            lines.append(f"## {enrich['displayName']} ({func['short_name']})")
        else:
            lines.append(f"## {func['short_name']}")
        lines.append("")

        # Description: prefer docblock, fall back to enrichment
        desc = extract_function_description(func)
        if not desc and enrich.get("description"):
            desc = enrich["description"]
        if desc:
            lines.append(desc)
            lines.append("")

        lines.append(f"**Signature:** `{sig}`")
        lines.append("")

        # Note about variant-preserving return types
        rtype = func.get("return_type", "")
        if rtype in ("static", "self", "$this"):
            lines.append("*Returns the same variant type as the caller (preserves subclass type).*")
            lines.append("")

        loc = f"{func['file']}:{func['line_start']}"
        if func["line_end"] != func["line_start"]:
            loc += f"-{func['line_end']}"
        lines.append(f"**Location:** `{loc}`")
        lines.append("")

        if func.get("namespace"):
            lines.append(f"**Namespace:** `{func['namespace']}`")
            lines.append("")
        if func.get("class_name"):
            lines.append(f"**Class:** `{func['class_name']}`")
            lines.append("")

        if func.get("attributes"):
            attrs = ", ".join(func["attributes"])
            lines.append(f"**Attributes:** `{attrs}`")
            lines.append("")

        # Enrichment metadata
        if enrich.get("library"):
            lines.append(f"**Library:** {enrich['library']}")
            lines.append("")

        if enrich.get("confidence") and enrich["confidence"] != "confident":
            lines.append(f"**Confidence:** {enrich['confidence']}")
            lines.append("")

        if enrich.get("tags"):
            tag_str = ", ".join(f"`{t}`" for t in enrich["tags"])
            lines.append(f"**Tags:** {tag_str}")
            lines.append("")

        if enrich.get("notes"):
            lines.append(f"*{enrich['notes']}*")
            lines.append("")

        lines.append("---")
        lines.append("")

    return "\n".join(lines)


def generate_index(
    categories: Dict[str, Dict[str, List[dict]]],
    project_name: str,
    third_party_count: int,
    taxonomy: dict,
    enrichment: Optional[dict] = None,
) -> str:
    """Generate hierarchical project index markdown."""
    total_funcs = sum(
        len(funcs)
        for subcats in categories.values()
        for funcs in subcats.values()
    )
    total_subcats = sum(len(subcats) for subcats in categories.values())

    lines = [
        f"# {project_name} Function Map",
        "",
        f"**Total functions:** {total_funcs}",
    ]
    if third_party_count > 0:
        lines[-1] += f" (excludes {third_party_count} bundled third-party functions)"
    lines.append(f"**Categories:** {len(categories)} top-level, {total_subcats} subcategories")
    if enrichment and enrichment.get("entries"):
        entry_count = len(enrichment["entries"])
        source = enrichment.get("source", "unknown")
        lines.append(f"**Enrichment:** {entry_count} entries from {source}")
    lines.extend([
        "",
        "---",
        "",
    ])

    # Order categories: use taxonomy ordering if available, then alphabetical
    taxonomy_cats = list(taxonomy.get("categories", {}).keys())
    all_cats = set(categories.keys())

    # Taxonomy order first, then any extras alphabetically
    ordered_cats = [c for c in taxonomy_cats if c in all_cats]
    ordered_cats.extend(sorted(all_cats - set(ordered_cats)))

    for top_cat in ordered_cats:
        if top_cat not in categories:
            continue
        subcats = categories[top_cat]
        top_title = top_cat.replace("-", " ").replace("_", " ").title()
        top_desc = get_category_description(top_cat, taxonomy)
        top_count = sum(len(funcs) for funcs in subcats.values())

        lines.append(f"## {top_title}")
        lines.append("")
        lines.append(f"*{top_desc}*")
        lines.append("")
        sub_word = "subcategory" if len(subcats) == 1 else "subcategories"
        lines.append(f"**{top_count} functions** across {len(subcats)} {sub_word}:")
        lines.append("")

        for sub_name in sorted(subcats.keys()):
            funcs = subcats[sub_name]
            sub_title = _format_subcategory_title(sub_name)
            filename = f"{top_cat}--{sub_name}.md"
            sub_key = f"{top_cat}/{sub_name}"
            sub_desc = get_category_description(sub_key, taxonomy)

            if len(sub_desc) > 120:
                sub_desc = sub_desc[:117] + "..."

            lines.append(f"- **[{sub_title}]({filename})** ({len(funcs)}) -- {sub_desc}")

        lines.append("")

    lines.extend([
        "## Usage",
        "",
        "1. Browse categories above to find relevant functions",
        "2. Click subcategory link to see function signatures, descriptions, and locations",
        "3. Use location info to read source code for implementation details",
        "",
    ])

    return "\n".join(lines)


def generate_third_party_summary(
    third_party_funcs: List[dict], taxonomy: dict
) -> str:
    """Generate a summary file for excluded third-party code.

    Groups functions by which taxonomy rule excluded them, using the rule's
    'reason' field as the group label. Universal -- no hard-coded library names.
    """
    rules = taxonomy.get("routing_rules", {}).get("third_party", [])

    # Group functions by the matching rule's reason
    groups: Dict[str, List[dict]] = defaultdict(list)
    for fn in third_party_funcs:
        fp = fn["file"].replace("\\", "/")
        matched_reason = "Other"
        for rule in rules:
            if _matches_pattern(fp, fn, rule):
                matched_reason = rule.get("reason", rule.get("pattern", "Unknown"))
                break
        groups[matched_reason].append(fn)

    lines = [
        "# Third-Party Bundled Libraries",
        "",
        "These functions are from bundled third-party libraries and are excluded from the main function map.",
        "",
        f"**Total third-party functions:** {len(third_party_funcs)}",
        "",
        "---",
        "",
    ]

    for group_name, funcs in sorted(groups.items(), key=lambda x: -len(x[1])):
        files = sorted(set(fn["file"] for fn in funcs))
        lines.append(f"## {group_name} ({len(funcs)} functions)")
        lines.append("")
        lines.append(f"**Files:** {len(files)}")
        lines.append("")
        for f in files[:20]:
            lines.append(f"- `{f}`")
        if len(files) > 20:
            lines.append(f"- *...and {len(files) - 20} more files*")
        lines.append("")

    return "\n".join(lines)


